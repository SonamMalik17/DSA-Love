#include<iostream>
using namespace std;
int main()
{
    int a = 2/5;
    cout<<"a = "<<a<<endl<<endl;

    float b = 2/5;
    cout<<"b = "<<b<<endl<<endl;

    int c = 2.0/5;
    cout<<"c = "<<c<<endl<<endl; 

    float d = 2.0/5;
    cout<<"d = "<<d<<endl<<endl; 

    cout<<"2/5 = "<<2/5<<endl<<endl;

    cout<<"2.0/5 = "<<2.0/5<<endl<<endl;

    return 0;
}